//! Emission: the generated assembly, the catalog TSV, and the README error-code dictionary.

use core::fmt::Write as _;

use crate::dsl::Test;

/// Base address of the results block in WRAM (bank `$7E`).
///
/// The host harness reads this with `Bus::peek_wram`, which is explicitly non-intrusive — it
/// does not advance the clock, touch open bus, or trip register side effects.
pub const RESULTS_BASE: u32 = 0x7E_F000;

/// Offset of the one-byte-per-test status array within the results block, index-aligned to
/// `SOURCE_CATALOG.tsv`.
///
/// The full block layout — shared with `asm/runtime.inc` and the host harness, which must agree
/// on it byte for byte:
///
/// | Offset | Field |
/// |---|---|
/// | `+$00` | magic `"ACSN"` |
/// | `+$04` | format version (u16) |
/// | `+$06` | test count (u16) |
/// | `+$08` | `$A5` once every test has run |
/// | `+$0A` | passed / failed / skipped / golden (four u16s) |
/// | `+$20` | status array, one byte per test |
pub const STATUS_OFFSET: u32 = 0x20;

/// Groups whose test bodies live outside bank `$00`, and the segment each goes to.
///
/// Bank `$00` holds the runtime, the font's loader, the catalog and everything else; it has
/// overflowed three times as the battery grew. The dispatch table is 24-bit, so a body may live in
/// any bank — but only groups whose tests are *position-independent* may move, and the build gate
/// below enforces that by rejecting any bank-local `jsr`/`jmp` to a runtime label in a moved body.
///
/// Group `C` moved once the runtime grew `_far` wrappers (`wait_vblank_far`, `frame_step_far`,
/// `hv_begin_far`, `hv_end_far`) — the build gate below had rejected it while its bodies still
/// used a bank-local `jsr`. Group `D` would move the same way if bank `$00` fills again.
const OUT_OF_BANK: &[(char, &str)] = &[
    ('E', "TESTSE"),
    ('C', "TESTSC"),
    ('G', "TESTSG"),
    ('F', "TESTSG"),
];

/// Generate `tests_group_a.s` — every test body plus the dispatch table the runtime walks.
#[must_use]
pub fn asm(tests: &[Test]) -> String {
    let mut s = String::new();
    let _ = writeln!(s, "; GENERATED by accuracysnes-gen — do not edit by hand.");
    let _ = writeln!(
        s,
        "; Source of truth: tests/roms/AccuracySNES/gen/src/tests/"
    );
    let _ = writeln!(s, ".p816");
    let _ = writeln!(s, "CATALOG_IMPL = 1");
    let _ = writeln!(s, ".include \"runtime.inc\"");
    let _ = writeln!(s);
    let _ = writeln!(s, ".segment \"TESTS\"");
    let _ = writeln!(s);

    // Bank $00 holds the runtime, the catalog and the remaining test bodies, and it has now
    // overflowed three times. Groups that can move do: their tests reach everything through long
    // addressing or through the I/O registers, which are mirrored in every LoROM bank.
    //
    // **Group A deliberately does NOT move.** `A4.11`/`A4.12` aim an `(a,X)` indirect jump at the
    // *program bank's* signature block to tell an in-bank wrap from a bank carry; relocating them
    // would silently change which bank they probe, and the test would still pass while measuring
    // something else. Group B is likewise pinned — its access-speed rows depend on where they run.
    for t in tests
        .iter()
        .filter(|t| !OUT_OF_BANK.iter().any(|(g, _)| *g == t.group))
    {
        let _ = writeln!(s, "{}", t.body);
    }
    for (group, segment) in OUT_OF_BANK {
        let _ = writeln!(s, ".segment \"{segment}\"");
        let _ = writeln!(s);
        for t in tests.iter().filter(|t| t.group == *group) {
            // A body outside bank $00 may not reach the runtime with a bank-local `jsr`: the
            // same 16-bit address in another bank is not a subroutine, it is whatever bytes are
            // there. The failure is silent and total — every test in the group reported a garbage
            // verdict when one `jsr apu_upload` survived the move — so it is a build error.
            for line in t.body.lines() {
                let op = line.trim();
                let target = op.strip_prefix("jsr ").or_else(|| op.strip_prefix("jmp "));
                if let Some(target) = target {
                    // `@`-prefixed targets are cheap locals inside the same `.proc`, so they are in
                    // this bank by construction. Anything else names a runtime label.
                    assert!(
                        target.starts_with('@') && op.starts_with("jmp "),
                        "{}: an out-of-bank test body must reach the runtime with `jsl`/`jml`, not \
                     `{op}`",
                        t.id
                    );
                }
            }
            let _ = writeln!(s, "{}", t.body);
        }
    }

    // Out-of-bank read-only data: the SPC700 program images. They are pure data, they are read
    // through a 24-bit pointer by `apu_upload`, and they are large enough that keeping them in
    // bank $00 alongside every test body overflowed it.
    let data: String = tests.iter().map(|t| t.data.as_str()).collect();
    if !data.is_empty() {
        let _ = writeln!(s, ".segment \"APUDATA\"");
        let _ = writeln!(s, "{data}");
    }

    // The dispatch table: one entry per test, walked by the runtime.
    let _ = writeln!(s, ".segment \"CATALOG\"");
    let _ = writeln!(s, ".export _test_count");
    let _ = writeln!(s, ".export _test_entries");
    let _ = writeln!(s, ".export _test_names");
    let _ = writeln!(s, ".export _test_flags");
    let _ = writeln!(s);
    let _ = writeln!(s, "_test_count:");
    let _ = writeln!(s, "    .word {}", tests.len());
    let _ = writeln!(s);
    let _ = writeln!(
        s,
        "; Entry points, 24-bit: test bodies no longer all live in bank $00."
    );
    let _ = writeln!(s, "_test_entries:");
    for t in tests {
        let _ = writeln!(s, "    .faraddr {}", t.label());
    }
    let _ = writeln!(s);
    let _ = writeln!(
        s,
        "; Per-test flags: bit0 = scores toward the pass rate, bit1 = golden-vector."
    );
    let _ = writeln!(s, "_test_flags:");
    for t in tests {
        let scores = u8::from(t.provenance.scores() && t.kind == crate::dsl::Kind::Scored);
        let golden = u8::from(t.kind == crate::dsl::Kind::Golden) << 1;
        let _ = writeln!(s, "    .byte ${:02X}   ; {}", scores | golden, t.id);
    }
    let _ = writeln!(s);
    let _ = writeln!(s, "; Menu labels, each a length-prefixed ASCII string.");
    let _ = writeln!(s, "_test_names:");
    for t in tests {
        let _ = writeln!(s, "    .addr @n_{}", t.id.to_lowercase().replace('.', "_"));
    }
    for t in tests {
        let name = t.name;
        assert!(name.len() <= 24, "menu label '{name}' exceeds 24 columns");
        assert!(
            name.is_ascii(),
            "menu label '{name}' must be ASCII (the font covers $20-$7A only)"
        );
        let _ = writeln!(s, "@n_{}:", t.id.to_lowercase().replace('.', "_"));
        let _ = writeln!(s, "    .byte {}", name.len());
        let _ = writeln!(s, "    .byte \"{name}\"");
    }
    s
}

/// Generate `SOURCE_CATALOG.tsv`, the machine-readable manifest the harness `include_str!`s.
///
/// Columns: index, id, group, name, provenance tier, citation, kind, result address, and the
/// dossier assertion(s) the test implements (`-` when it implements none — see
/// [`crate::dossier::UNENUMERATED`]).
#[must_use]
pub fn catalog(tests: &[Test]) -> String {
    let mut s = String::new();
    let _ = writeln!(s, "# GENERATED by accuracysnes-gen — do not edit by hand.");
    let _ = writeln!(
        s,
        "# index\tid\tgroup\tname\ttier\tcitation\tkind\tresult_addr\tdossier"
    );
    for (i, t) in tests.iter().enumerate() {
        let addr = RESULTS_BASE + STATUS_OFFSET + u32::try_from(i).expect("test count fits u32");
        let kind = match t.kind {
            crate::dsl::Kind::Scored => "Scored",
            crate::dsl::Kind::Golden => "Golden",
        };
        let mapped = crate::dossier::for_test(t.id);
        let dossier = if mapped.is_empty() {
            "-".to_string()
        } else {
            mapped.join(",")
        };
        let _ = writeln!(
            s,
            "{i}\t{}\t{}\t{}\t{}\t{}\t{kind}\t0x{addr:06X}\t{dossier}",
            t.id,
            t.group,
            t.name,
            t.provenance.tier(),
            t.provenance.cite(),
        );
    }
    s
}

/// Generate the README's error-code dictionary — the thing that makes a `FAIL` actionable for
/// an emulator author rather than merely discouraging.
#[must_use]
pub fn readme_codes(tests: &[Test]) -> String {
    let mut s = String::new();
    let _ = writeln!(
        s,
        "<!-- GENERATED by accuracysnes-gen — do not edit by hand. -->"
    );
    let _ = writeln!(s);
    // Gather the groups in first-appearance order, then emit each group's tests together.
    //
    // The battery is NOT ordered by group -- later batches append to the end, so group A tests sit
    // either side of group C's -- and this used to emit a `## Group X` heading every time the
    // group *changed*, producing the same heading several times. That is a duplicate-heading lint
    // failure and, worse, a reference document where "## Group A" does not contain all of group A.
    let mut groups: Vec<char> = Vec::new();
    for t in tests {
        if !groups.contains(&t.group) {
            groups.push(t.group);
        }
    }
    for group in groups {
        let _ = writeln!(s, "## Group {group}");
        let _ = writeln!(s);
        for t in tests.iter().filter(|t| t.group == group) {
            let _ = writeln!(s, "### {} — {}", t.id, t.name);
            let _ = writeln!(s);
            let _ = writeln!(
                s,
                "Provenance: **{}** ({}). Kind: {}.",
                t.provenance.tier(),
                t.provenance.cite(),
                match t.kind {
                    crate::dsl::Kind::Scored => "scored",
                    crate::dsl::Kind::Golden => "golden vector, never scored",
                }
            );
            let _ = writeln!(s);
            if t.codes.is_empty() {
                // A test with no allocated codes cannot fail. Which of the two reasons applies matters
                // to anyone reading a result: a golden vector reports *which* behaviour it saw and is
                // never scored, while a plain test simply has nothing to assert beyond completing.
                let _ = writeln!(
                    s,
                    "{}",
                    match t.kind {
                        crate::dsl::Kind::Golden =>
                            "No failure codes — this is a **golden vector**. It cannot fail: it \
                         records what it observed and is excluded from the pass rate. Where the \
                         observation fits in a byte it goes in the verdict as a variant code \
                         (`(variant << 1) | 1`); where it does not — a dot count, say — the \
                         verdict is a plain pass and the value goes to the measurement channel \
                         at `$7E:E200`, which the host harness reads and prints. See the test's \
                         entry in `SOURCE_CATALOG.tsv` for its provenance tier and the reason it \
                         records rather than asserts.",
                        crate::dsl::Kind::Scored =>
                            "No failure codes (control-flow test: reaching the end is the pass).",
                    }
                );
            } else {
                let _ = writeln!(s, "| Code | Byte | Meaning |");
                let _ = writeln!(s, "|---|---|---|");
                for (code, why) in &t.codes {
                    let _ = writeln!(s, "| {code} | `${:02X}` | {why} |", code << 1);
                }
            }
            let _ = writeln!(s);
        }
    }
    s
}
