/// Render the font to `GLYPHS * 8` bytes, indexed by ASCII code.
///
/// Lowercase letters reuse the uppercase glyphs: the menu labels are mixed-case, and duplicating
/// the bitmaps keeps the font small while staying legible. Tile identity is still exactly ASCII.
#[must_use]
pub fn render() -> Vec<u8> {
    let mut out = vec![0u8; GLYPHS * 8];
    for (ch, glyph) in table() {
        let mut rows = [0u8; 8];
        for (r, pattern) in glyph.iter().enumerate() {
            let mut bits = 0u8;
            for (c, px) in pattern.chars().enumerate() {
                if px == '#' {
                    // Bit 7 is the leftmost pixel; the 5-wide cell sits at bits 7..3.
                    bits |= 0x80 >> c;
                }
            }
            rows[r] = bits;
        }
        let base = usize::from(ch) * 8;
        out[base..base + 8].copy_from_slice(&rows);
        // Mirror A-Z into a-z.
        if ch.is_ascii_uppercase() {
            let lower = usize::from(ch.to_ascii_lowercase()) * 8;
            out[lower..lower + 8].copy_from_slice(&rows);
        }
    }
    out
}

/// Emit the font as a ca65 source file.
#[must_use]
pub fn asm() -> String {
    use core::fmt::Write as _;
    let data = render();
    let mut s = String::new();
    let _ = writeln!(s, "; GENERATED by accuracysnes-gen — do not edit by hand.");
    let _ = writeln!(s, "; 8x8 font, one byte per row, bit 7 = leftmost pixel.");
    let _ = writeln!(s, "; Indexed by ASCII so that tile_index & $FF == ASCII.");
    let _ = writeln!(s, ".p816");
    let _ = writeln!(s, ".export font_data");
    assert_eq!(data.len(), 1024, "FONT_SIZE in runtime.inc must match");
    let _ = writeln!(s, ".segment \"FONT\"");
    let _ = writeln!(s, "font_data:");
    for (i, chunk) in data.chunks(8).enumerate() {
        let bytes: Vec<String> = chunk.iter().map(|b| format!("${b:02X}")).collect();
        let ch = u8::try_from(i).unwrap_or(0);
        let label = if (0x20..0x7F).contains(&ch) {
            format!("  ; '{}'", ch as char)
        } else {
            String::new()
        };
        let _ = writeln!(s, "    .byte {}{label}", bytes.join(","));
    }
    s
}
