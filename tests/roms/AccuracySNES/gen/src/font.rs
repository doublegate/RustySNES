//! An 8x8 bitmap font, generated rather than vendored.
//!
//! Written from scratch so the cartridge stays cleanly MIT OR Apache-2.0 with no third-party
//! font licence to carry. Glyphs are 5x7 inside an 8x8 cell, left-aligned (bit 7 = leftmost
//! pixel), which is the orientation SNES tiles want; the popular public-domain `font8x8` is
//! LSB-first and would need bit-reversing.
//!
//! The font is indexed by raw ASCII: tile *n* holds the glyph for character *n*. That is what
//! makes `tile_index & 0xFF == ASCII` hold in the tilemap, so a host can recover on-screen text
//! straight out of VRAM without OCR. Unmapped codes render blank.

/// Number of glyphs emitted (ASCII `$00`-`$7F`), 8 bytes each.
pub const GLYPHS: usize = 128;

